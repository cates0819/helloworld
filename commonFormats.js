function onhover(e){
  if (!e) var e = window.event;
  //e refers to the event
  
  var targ;
  if (e.target) targ = e.target;
	else if (e.srcElement) targ = e.srcElement;
	if (targ.nodeType == 3) // defeat Safari bug
		targ = targ.parentNode;
	
	if (targ.style.backgroundColor == ""){
	  targ.style.backgroundColor = '#D0D3D4';
	}
}

function offhover(e){
  if (!e) var e = window.event;
  //e refers to the event
  
  var targ;
  if (e.target) targ = e.target;
	else if (e.srcElement) targ = e.srcElement;
	if (targ.nodeType == 3) // defeat Safari bug
		targ = targ.parentNode;
	
	if (targ.style.backgroundColor == 'rgb(208, 211, 212)'){
	  targ.style.backgroundColor = "";
	}
}

function dropdownClick(e){
  if (!e) var e = window.event;
  //e refers to the event
  
  var targ;
  if (e.target) targ = e.target;
	else if (e.srcElement) targ = e.srcElement;
	if (targ.nodeType == 3) // defeat Safari bug
		targ = targ.parentNode;
	
	
	if (targ.content.style.display =="none"){
	  targ.content.style.display = "block";
	}else{
	  targ.content.style.display = "none";
	}
}

